package java01;

public class J20220215_02_datatype {

	public static void main(String[] args) {
		// 자료형
		//정수형(int:4byte)
		System.out.println(10);
		//실수형(double:8byte)
		System.out.println(3.14);
		//문자형(2byte)
		//자바는 유니코드값:한글자당 2byte
		System.out.println('홍'+0);//코드값 알아내기
		//문자열:형은 없다,참조형
		System.out.println("hello" + (10+20));
		
		
		
	}

}
